package com.cronit.util;

public class ConsoleColors {
    // RESET for resetting to default color
    public static final String RESET = "\033[0m";

    // Text colors  
    public static final String RED = "\033[0;31m";  // For errors
    public static final String GREEN = "\033[0;32m";    // For success messages
    public static final String YELLOW = "\033[0;33m";   // For warnings
    public static final String CYAN = "\033[0;36m"; // For informational messages
}
